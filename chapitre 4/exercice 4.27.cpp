#include <iostream>
using namespace std;

int main(){
    cout << (3UL & 7UL) << '\n' << (3UL | 7UL) << '\n' << (3UL && 7UL) << '\n' << (3UL || 7UL) << endl;
    return 0;
}
/*
3UL : 00000000 00000000 00000000 00000011
7UL : 00000000 00000000 00000000 00000111

3UL & 7UL : 00000000 00000000 00000000 00000011 结果是3
3UL | 7UL : 00000000 00000000 00000000 00000111 结果是7

另外两个是逻辑运算符，但凡不是0的进去都是true
加括号是因为优先级问题
*/